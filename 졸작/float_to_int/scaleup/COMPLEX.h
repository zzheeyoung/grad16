complex multiple(complex x, complex y)
{
	complex result;

	result.re = (x.re*y.re - x.im*y.im) >> RX_FFT_MULT_SCALE_DN;
	result.im = (x.re*y.im + x.im*y.re) >> RX_FFT_MULT_SCALE_DN;

	return result;
}

complex add_cal(complex x, complex y)
{
	complex result;

	result.re = x.re + y.re;
	result.im = x.im + y.im;

	return result;
}

complex sub_cal(complex x, complex y)
{
	complex result;

	result.re = x.re - y.re;
	result.im = x.im - y.im;

	return result;
}
